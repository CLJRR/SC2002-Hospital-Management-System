package enums;

/**
 * The {@code Gender} enum represents the gender categories that can be assigned
 * to a user or entity in the system.
 * This enum allows for standardized representation of gender across the
 * application.
 */

public enum Gender {
    /**
     * Represents the male gender.
     */
    MALE,

    /**
     * Represents the female gender.
     */
    FEMALE,

    /**
     * Represents any gender identity that does not fall under male or female
     * categories.
     */
    OTHER
}
